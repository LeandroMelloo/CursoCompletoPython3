# Operadores

# Criando duas variaveis 'a' 'b' e comparando valores.
a = 1
b = 2

# Printando a comparação entre as variaveis com o método igual => ==.
print(a == b) # False.

# Printando a comparação entre as variaveis com o método diferente => !=.
print(a != b) # True.

# Printando a comparação entre as variaveis com o método menor => <.
print(a < b) # True.

# Printando a comparação entre as variaveis com o método menor => >.
print(a > b) # False.

# Printando a comparação entre as variaveis com o método menor ou igual => <=.
print(a <= b) # True.

# Printando a comparação entre as variaveis com o método maior ou igual => >=.
print(a >= b) # False.