# Declarando uma variavel em python => a.
a = 1

# Printando uma variavel => a.
print(a)

# Declarando uma variavel em python => b.
b = 2

#Printando uma variavel => b.
print(b)

# somando duas variaveis => a + b = 3.
print(a + b)

# subtraindo duas variaveis => a - b = -1.
print(a - b)

# multiplicando duas variaveis => a * b = 2.
print(a * b)

# dividindo duas variaveis => a / b = 0.5.
print(a / b)

# declarando uma variavel receita = 1000.
receita = 100

# printando a variavel receita.
print(receita)

# declarando uma variavel margem_de_lucro = 0.10.
margem_de_lucro = 0.88

# printando a variavel margem_de_lucro.
print(margem_de_lucro)

# declarando uma variavel lucro = receita * margem_de_lucro.
lucro = receita * margem_de_lucro

# printando a variavel lucro.
